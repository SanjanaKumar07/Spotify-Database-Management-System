    UserID INT PRIMARY KEY,
    UserName VARCHAR(100) NOT NULL,
    Email VARCHAR(255),
    Gender VARCHAR(10),
    ListeningDevice VARCHAR(100),
	Region VARCHAR(100),
    Followers INT,
    SubscriptionID INT,
	PaymentID INT
)AS NODE;
